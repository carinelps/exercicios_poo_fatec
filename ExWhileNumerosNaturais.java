public class ExWhileNumerosNaturais {
    public static void main(String[] args) throws Exception {

        /* 1. Escreva um programa que exibe os primeiros 100 números naturais. 
        Faça versões usando for, while e do/while. */

        /*Versão WHILE*/
        
        int i = 0;
        while(i <= 100){
            System.out.print("\n");
            System.out.print(i);
            i++;
        }  
     }
}
