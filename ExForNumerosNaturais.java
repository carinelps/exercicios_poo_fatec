
public class ExForNumerosNaturais {
    public static void main(String[] args) throws Exception {

        /* 1. Escreva um programa que exibe os primeiros 100 números naturais. 
        Faça versões usando for, while e do/while. */

        /*Versão FOR*/
  
        for (int i = 0; i <= 100; i++){
            System.out.print("\n");
            System.out.print(i);
        } 

     }
 }
