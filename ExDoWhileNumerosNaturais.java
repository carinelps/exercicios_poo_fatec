public class ExDoWhileNumerosNaturais {
    public static void main(String[] args) throws Exception {

        /* 1. Escreva um programa que exibe os primeiros 100 números naturais. 
        Faça versões usando for, while e do/while. */

        /*Versão DO WHILE*/
        
        int i = 0;

        do{
            System.out.print("\n");
            System.out.print(i);
            i++;
            
        }
        while(i <= 100);
            
     }
}

